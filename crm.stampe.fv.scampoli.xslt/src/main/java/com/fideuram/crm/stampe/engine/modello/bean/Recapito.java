package com.fideuram.crm.stampe.engine.modello.bean;

public class Recapito {
	//valori
	private String via;
	private String citta;
	private String provincia;
	private String cap;
	private String nazione;
	//descrizioni
	private String descVia;
	private String descCitta;
	private String descProvincia;
	private String descCap;
	private String descNazione;
	
	
	public String getVia() {
		return via;
	}
	public void setVia(String via) {
		this.via = via;
	}
	public String getCitta() {
		return citta;
	}
	public void setCitta(String citta) {
		this.citta = citta;
	}
	public String getProvincia() {
		return provincia;
	}
	public void setProvincia(String provincia) {
		this.provincia = provincia;
	}
	public String getCap() {
		return cap;
	}
	public void setCap(String cap) {
		this.cap = cap;
	}
	public String getNazione() {
		return nazione;
	}
	public void setNazione(String nazione) {
		this.nazione = nazione;
	}
	public String getDescVia() {
		return descVia;
	}
	public void setDescVia(String descVia) {
		this.descVia = descVia;
	}
	public String getDescCitta() {
		return descCitta;
	}
	public void setDescCitta(String descCitta) {
		this.descCitta = descCitta;
	}
	public String getDescProvincia() {
		return descProvincia;
	}
	public void setDescProvincia(String descProvincia) {
		this.descProvincia = descProvincia;
	}
	public String getDescCap() {
		return descCap;
	}
	public void setDescCap(String descCap) {
		this.descCap = descCap;
	}
	public String getDescNazione() {
		return descNazione;
	}
	public void setDescNazione(String descNazione) {
		this.descNazione = descNazione;
	}	
}